package com.agitrubard.authside.auth.domain.permission.enums;

/**
 * The {@code PermissionCategory} enum represents different categories or types of permissions in the authentication system.
 * These categories are used to classify and manage permissions related to user management and role management.
 *
 * @author Agit Rubar Demir | @agitrubard
 * @version 1.0.0
 */
public enum AuthSidePermissionCategory {

    /**
     * Represents permissions related to user management, such as creating, updating, or deleting users.
     */
    USER_MANAGEMENT,

    /**
     * Represents permissions related to role management, such as creating, updating, or deleting roles.
     */
    ROLE_MANAGEMENT

}
